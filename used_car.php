<p>Inspection and Test Drive by YOU</p>

<p>Bring two people. One of these people should be mechanically inclined.</p>

<p><b><br />
</b></p>

<p>Bring rags or paper towels to clean hands and wipe fluids on</p>

<p>Bring OBD code scanner</p>

<p>Bring sheet or tarp to lay on when looking under the car</p>

<p>Bring mirror on a stick (sold at AutoZone, etc.) for checking for rust/leaks under car.</p>

<p>Wear old clothes that you won't mind getting oily.</p>

<p>Know location of your mechanic that will do the inspection if needed. You will set up an appointment to have the car checked if your own inspection goes well.</p>

<p>Bring this checklist</p>

<p>After the inspection, if the car looks good to you don't delay - others will be looking at the car also. At the same time, don't appear over eager, this could hurt you when negotiating the price.</p>

