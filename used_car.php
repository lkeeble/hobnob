</b></p>

<p><b>The Inspection by You</p></p>

<p>[ ] Do this first to potentially save a couple of hours of your time. INSPECT THE TITLE. Make sure they are the owner. Make sure the car is NOT SALVAGED. If salvaged, move on. It's been in a serious accident. </p>

<p>[ ] Look at the car closely all around it for signs of accident damage or flood damage. If the car smells musty walk away due to risk of being flooded. (Has the owner sprayed scent or is using an air freshener to cover this up?)</p>

<p>[ ] What's your overall impression of car? If looks very rough, it has probably not been well maintained. Move on.</p>

<p>[ ] Check mileage. Does it jive with the advertisement?</p>

<p>[ ] Look from about 15 feet in front and behind, make sure wheels on both sides are at the same location, left and right side match.</p>

<p>[ ] Check body panels for paint mismatch or bad paint job. </p>

<p>[ ] Check body panel gaps to make sure they are all consistent. If not, suspect serious accident, walk away. </p>

<p>[ ] Run hand over any areas that look mismatched to see if the paint surface is different from the factory paint, ask about any such area. If you suspect a serious accident, walk away.</p>

<p>[ ] Check all interior trim, including the roof trim.</p>

<p>[ ] Condition of front seats?</p>

<p>[ ] Condition of rear seats, and rear bench if included? </p>

<p>[ ] Check that all seat belts work.</p>

<p>[ ] Open and shut all doors and trunk and hatch. Check they all fit properly when closed. If not, move on. Suspect serious accident.</p>

<p>[ ] Have a look in the glove compartment. Is the owner's manual in there? </p>

<p>[ ] Smell inside the car. Does it smell mouldy/musty? If so, move on. Suspect flood damage. </p>

<p>[ ] Check any automatically opening doors or hatch. Do they all work? </p>

<p>[ ] Check inside the trunk. Pull up the carpeting and look underneath for signs of rust (flood damage). Move on if found.</p>

<p>[ ] Does car have jack and spare tire? If not, consider moving on, sign of poor maintenance. </p>

<p>[ ] Check for rust on outside of car. Does rust go all the way through? If major, move on. </p>

<p>[ ] Check all windows for cracks and chips.</p>

<p>[ ] Look under the car for rust. If major, move on. </p>

<p>[ ] Check clearance between tires and body on both sides. Should match, if not, move on (potential accident damage). </p>

<p>[ ] Look under the engine area for leaks. Engine, transmission, differential. Could be engine oil, coolant, or other. If found, move on. </p>

<p>[ ] Any dented metal under the car (something was run over, potentially causing serious damage to engine or transmission)? </p>

<p>[ ] Welds look OK under the car? Otherwise, suggests major repair work (accident) - move on.</p>

<p>[ ] Check on ground under the car for puddles or stains of fluid.</p>

<p>[ ] Check bottom of radiator for signs of coolant/leaks.</p>

<p>[ ] Check for cracks in the serpentine belt</p>

<p>[ ] Check to see if vehicle coolant is cool (touch hoses near radiator). If cool, open the radiator cap with a rag. Does coolant come all the way to the top? If not, suspect leak. Check coolant reservoir. Is level between min/max?</p>

<p>[ ] Check engine oil dipstick. Does oil look clean? If milky, sign of blown head gasket. Move on. </p>

<p>[ ]What is oil level? If significantly below max, move on. Sign of either oil leak, engine burning oil or poor engine maintenance.</p>

<p>[ ] Check brake fluid level. Should be between min/max.</p>

<p>[ ] Start the car. Does it start easily? If not, move on. </p>

<p>[ ] Any strange noises such as clacking noises coming from the engine? If so, walk away.</p>

<p>[ ] Do warning lights all come on when car is first started, then go out?</p>

<p>[ ] Which warning lights remain on? Check engine light?</p>

<p>[ ] Is there any tape over the dash to hide warning lights? </p>

<p>[ ] Does the airbag light stay on or blink?</p>

<p>[ ] Let the car idle for about 5 minutes. Does it idle smoothly? If not, move on. There are many reasons for a car to not idle smoothly, some of which can be very expensive to fix.</p>

<p>[ ] Scan codes and see if any are found. If so, write them down to check. If the scan tool won't communicate with the car's computer, walk away. Could be broken computer (very expensive).</p>

<p>[ ] With the OBD scanner, check the "OBD state". See if there are any "incomplete" monitor states. If they are then the codes have likely been reset recently by the seller. So you won't get a true picture of all the potential problems. Walk away. See <a href=https://youtu.be/XtxG7CODbH8?t=600">Scotty Kilmer's advice</a>

<p>[ ] Have friend rev the car. Any strange noises from the engine? If so, walk away.</p>

<p>[ ] With friend revving the engine, check exhaust. Does it look clean? If persistent black or white smoke (not just mist because it's cold) move on. </p>

<p>[ ] Open the hood. Listen to the engine. Does it sound smooth? If not, move on.</p>

<p>[ ] Is engine area covered in oil? If so, move on.</p>

<p>[ ] Check for oil change stickers that confirm oil changes or indicate an oil change is long overdue.</p>

<p>[ ] Look for any signs of leaks - oil, coolant, brake fluid, etc. </p>

<p>[ ] When engine is OFF, check radiator hoses for signs of wear. With care not to get burnt, press and release, should spring back to shape.</p>

<p>[ ] Check transmission fluid AFTER the test drive. Is it red or light red? If smells burnt or has small black pieces of metal, move on. Will have transmission problem. </p>

<p>[ ] How does the battery look? Terminals clean? </p>

<p>[ ] Age of battery? </p>

<p>[ ] Get inside car, check that heat blows hot.</p>

<p>[ ] Check that heat/cooling fan works and is strong at maximum speed. </p>

<p>[ ] Check that air is blowing out of all vents.</p>

<p>[ ] Check A/C blows very cold. </p>

<p>[ ] Check tires for wear.</p>

<p>[ ] Do all tires have the correct number of lug nuts? </p>

<p>[ ] Check tires for uneven wear. </p>

<p>[ ] All hubcaps in place?</p>

<p>[ ] Check all power windows. Working and do they go up/down fast enough? </p>

<p>[ ] Check power mirror adjustment. </p>

<p>[ ] Check backup camera, if equipped.</p>

<p>[ ] Check overhead mirror adjustment. </p>

<p>[ ] Check windshield wipers, sprayer. </p>

<p>[ ] Check rear windshield wipers, sprayer. </p>

<p>[ ] If possible, check that rear window defrost works. </p>

<p>[ ] Are windshield wipers in good condition?</p>

<p>[ ] Adjust seats.</p>

<p>[ ] Adjust steering wheel if equipped.</p>

<p>[ ] Check interior lights, dome lights, reading lights, etc.</p>

<p>[ ] Check stereo - radio, CD player. </p>

<p>[ ] Turn up volume, make sure speakers aren't blown</p>

<p>[ ] Check horn.</p>

<p>[ ] Check parking brake.</p>

<p>[ ] Check indicators on all sides.</p>

<p>[ ] Check emergency flashers.</p>

<p>[ ] Check headlights, normal and high beam.</p>

<p>[ ] Check fog lights if equipped.</p>

<p>[ ] Check brake lights.</p>

<p>[ ] Check tail lights.</p>

<p>[ ] (with friend) Check reverse light.</p>

<p>[ ] Check that car locks/unlocks, using interior, exterior locks and remote lock/unlock.</p>

<p>[ ] How many keys and remote keys are available</p>

