<h1>
Checklist for Buying  a Used Car
</h1>

<p><b>Good videos to watch first</b></p>
<p>
<a href="https://www.youtube.com/watch?v=XtxG7CODbH8">Scotty Kilmer's advice on when to walk away from a used car</a>
</p>

<p><b>Preliminary</b></p>

<p>Give your insurance company a heads up that you are looking to buy a used car. Make sure that it will be insured for you to drive it right away when you buy one. Also check that it will be insured for you to test drive.</p>

<p><b><br />
</b></p>

<p>Don't trust the seller. Check EVERYTHING yourself.</p>

<p><b><br />
</b></p>

<p>Figure out what car you need</p>

<p><b><br />
</b></p>

<p>Figure out what car you want to buy. Narrow it down to a few makes/models. Toyota generally most reliable, next is Honda. But it depends on model/year. Check YouTube for car recommendations from mechanics. Base models have less things to go wrong than fully loaded models. Frugal buyers should favor simplicity over bells and whistles. Use kbb.com to get an idea of used car prices. Check craigslist to get another idea of prices in your local area for the vehicle your are interested in.</p>
<p>Do you want manual shift or automatic?</p>
<p>If you live in an area that gets snow and ice, you might want to pick a front-wheel drive car because they handle better in snowy conditions. Check if the car you are looking at has front or rear wheel drive.</p>


<p>Also see: <a href="https://carbuyingsupport.com/best-used-cars-to-buy/" >https://carbuyingsupport.com/best-used-cars-to-buy/</a></p>

<p><b><br />
</b></p>

<p>It's better to have a car with a NON INTERFERENCE engine. If the car has a timing belt, that can break and with an interference engine it can ruin the engine. If the car has a timing belt and an interference engine the timing belt must be changed regularly (for some models, every 60k miles, but depends on model). If you cannot tell when a used car has had its timing belt changed, you should change it immediately upon buying the car which will be EXPENSIVE ($600-$800 usually). Factor this in to the price of the car.</p>

