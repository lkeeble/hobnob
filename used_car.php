<p>The Mechanic's inspection</p>

<p>You must find a mechanic near you that you trust. The seller must bring their car to YOUR MECHANIC for a complete inspection. Typically around $100. If inspection is favorable then you should feel comfortable making an offer for the car. The mechanic's report should include all items that should be fixed and should give priority to the most expensive items in the car:</p>

<ol ><li><p>Engine</p>

</li>
<li><p>Transmission</p>

</li>
<li><p>Evidence of being in a serious wreck</p>

</li>
<li><p>Other items in order of expected expense</p>

</li>
</ol><p><b><br />
</b></p>

<p>Give the mechanic a list of the issues that you found during your inspection and ask that they estimate the cost of fixing each issue.</p>

<p><b><br />
</b></p>

<p>Any problems found will either give you reason to avoid buying the car and/or leverage to reduce the price. </p>

<p><b><br />
</b></p>

<p>Haggling</p>

<p><b><br />
</b></p>

<ol ><li><p>Don't seem desperate to buy the car.</p>

</li>
<li><p>Know the approximate price for this car using kbb.com or Edmunds.com.</p>

</li>
<li><p>Deduct some reasonable amount for the repairs that you know the car will need, based on the inspections.</p>

</li>
<li><p>Have a number in mind above which you will not go. </p>

</li>
<li><p>Aim for a number significantly below this price. </p>

</li>
<li><p>Go back and forth with the buyer as needed, not going above your price in step 2. </p>

</li>
<li><p>Be prepared to walk away. There are other cars.</p>

</li>
</ol><p><b><br />
</b></p>

<p>The sale</p>

<p><b><br />
</b></p>

<p>Consider using an escrow service to transfer money/title. Escrow.com? </p>

<p>Consider meeting the seller at your bank. As the bank to give you a room or space to do the transaction. Get a cashier's check for the amount. Only give it to the seller once they have signed over the title to you. They must sign and put in the sale amount.</p>

<p><b><br />
</b></p>

<p>After the sale</p>

<p><b><br />
</b></p>

<p>Let your insurance company know so they can add the car to your policy.</p>

<p>Transfer the car title to yourself through your local bureau of motor vehicles.</p>

<p>Get your car into your mechanic to do any of the “must do” items that were found during inspection.</p>

<p><br />
</p>

<p>Then...enjoy your new (used) car!!</p>

