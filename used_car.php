<p>Test Drive </p>

<p>Ideally the seller (owner) should allow you and your friend to drive in the car by yourselves. If that is not possible, ask them (and your friend!) to not talk during the test drive. You are all there to check the car, not socialize. </p>

<p><b><br />
</b></p>

<p>[ ] Check transmission, shifting between gears. Forward AND reverse. When starting, accelerating, decelerating, and when driven fast (on the highway preferably). If not shifting smoothly, walk away.</p>

<p>[ ] Does the car accelerate smoothly? Accelerating up a hill is a good test of a transmission. If there's problems, clunking, etc. walk away.</p>

<p>[ ] Change from park to drive. Does it clunk? If so, move on.</p>

<p>[ ] Change from park to reverse. Does it clunk? If so, move on.</p>

<p>[ ] Drive in a tight circle. Any clunking or noise?</p>

<p>[ ] Reverse the vehicle. Reverses smoothly? How is the visibility when reversing? </p>

<p>[ ] Drive over some bumps. How's suspension feel? Should be firm but not jarring. </p>

<p>[ ] Does anything squeak etc. when you go over bumps? </p>

<p>[ ] Do you hear any knocking noises from the engine that get faster with high engine speed? If so, walk away. </p>

<p>[ ] Does the car feel tired, as if it's worn out? Walk away.</p>

<p>[ ] Is steering wheel comfortable for you to hold? Not off center? </p>

<p>[ ] Drive in traffic and on the highway as fast as you can reasonably go in the time you have.</p>

<p>[] Does the cruise control work (if installed) or do things get jerky? If so, could be transmission problem. Walk away.

<p>[ ] Does power steering work?</p>

<p>[ ] Does steering wobble at all or some speeds? </p>

<p>[ ] Does vehicle pull to one side when you take hands briefly off the steering wheel?</p>

<p>[ ] Brake hard. Does car stop quickly? If not, move on. </p>

<p>[ ] Press on brake and hold it. Does brake slowly move to the ground? </p>

<p>[ ] Try stepping on the gas to get it to accelerate quickly. Does the transmission handle it OK? If not, move on.</p>

<p>[ ] Check temperature dial on the dash. It should settle in the middle of the cold/hot range. If not, move on. </p>

<p>[ ] Is car noisy when going fast or in traffic? </p>

<p>[ ] Is the car comfortable and enjoyable for you to drive? </p>

